# Variables

# Variable de Tipo String
nombre = "Renzo"
print(type(nombre))
mensaje = f"Bienvenido {nombre}"
print(mensaje)

# Variable de Tipo Integer
edad = 25
print(type(edad))
mensaje = f"Tienes {edad} anios."
print(mensaje)

peso = 84.4
print(type(peso))
mensaje = f"Tienes {peso} kilos."
print(mensaje)

# Variable de Tipo Boolean
esActivo = True
print(type(esActivo))
mensaje = f"Esta activo: {esActivo}"
print(mensaje)

numero1, numero2, numero3 = 1,2,3
print(f"El numero es: {numero1}")
print(f"El numero es: {numero2}")
print(f"El numero es: {numero3}")

exit(0)